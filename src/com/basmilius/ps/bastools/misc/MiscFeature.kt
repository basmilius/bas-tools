package com.basmilius.ps.bastools.misc

/**
 * Class MiscFeature
 *
 * @author Bas Milius
 * @package com.basmilius.ps.bastools.misc
 */
abstract class MiscFeature
{

	/**
	 * Initializes the feature.
	 *
	 * @author Bas Milius
	 */
	abstract fun init()

	/**
	 * Disposes the feature.
	 *
	 * @author Bas Milius
	 */
	abstract fun dispose()

}
